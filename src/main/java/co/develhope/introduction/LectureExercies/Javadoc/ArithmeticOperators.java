package co.develhope.introduction.LectureExercies.Javadoc;

public class ArithmeticOperators {
    /**
     *  method that takes 2 int parameters as input and returns their summation int
     * @param num1
     * @param num2
     * @return num1+num2
     */
    public int sum(int num1,  int num2){
        return num1 + num2;
    }

    /**
     * method that takes 2 int parameters as input and returns their subtraction int
     * @param num1
     * @param num2
     * @return num1-num2
     */
    public int sub(int num1,  int num2){
        return num1 - num2;
    }

    /**
     * method that takes 2 int parameters as input and returns their multiplication int
     * @param num1
     * @param num2
     * @return num1*num2
     */
    public int mul(int num1,  int num2){
        return num1 * num2;
    }

    /**
     * method that takes 2 int parameters as input and returns their division int
     * @param num1
     * @param num2
     * @return num1/num2
     */
    public int div(int num1,  int num2){
        return num1 / num2;
    }
}
